    Paint color;
    Stroke stroke;
    boolean relleno;
    List<Shape> vShape = new ArrayList();
    
    public void paint(Graphics g){
                      super.paint(g);
                      Graphics2D g2d = (Graphics2D)g;
                      g2d.setPaint(color);
                      g2d.setStroke(stroke);
                      //g2d.setComposite(); y otra mas
                      for(Shape s:vShape) {
                        if(relleno) g2d.fill(s);
                        g2d.draw(s);
                      }
}
    
